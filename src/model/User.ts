// model/User.ts
import mongoose, { Schema, models } from "mongoose";

interface IUser extends mongoose.Document {
  name: string;
  email: string;
  password: string;
}

const userSchema = new Schema<IUser>(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const User = models.User || mongoose.model<IUser>("User", userSchema);

export default User;